num = int(input('Введите целое число > 0: '))
biggest = 0

if num < 10:
    biggest = num
else:
    while num // 10 > 0:
        num1 = num % 10
        num2 = (num % 100) // 10
        if biggest < num1 and num1 >= num2:
            biggest = num1
        elif biggest < num2 and num2 >= num1:
            biggest = num2
        num = num // 10

