# task 5

income = int(input('Введите целое число > 0, обозначающее доход компании в рублях: '))
costs = int(input('Введите целое число > 0, обозначающее расход комании в рублях: '))
profit = income - costs

if profit > 0:
    print(f"Компания с заданными параметрами имеет прибыль равную {profit} руб.")
    print("Компания с заданными параметрами имеет рентбельность выручки {0:.2f}%.".format((profit / income) * 100))
    stuff = int(input('Введите целое число > 0, обозначающее количество сотрудников в компании: '))
    print("Компания с заданными параметрами имеет прибыль на 1 сотрудника равную {0:.2f} руб.".format(profit / stuff))
elif profit < 0:
    print(f"Компания с заданными параметрами имеет убыток равный {profit} руб.")
elif profit == 0:
    print(f"Ваша комания уникальна и тратит ровно столько же рублей, сколько получает.")

