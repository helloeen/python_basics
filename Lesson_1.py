run = int(input('Введите целое число > 0, обозначающее кол-во км, которое пробежал спортсмен в первый день: '))
goal = int(input('Введите целое число > 0, обозначающее кол-во км, которое спортсмен стремится пробегать за день : '))
days = 1

while run < goal:
    run *= 1.1
    days += 1

print(f"На {days}-й день спортсмен достигнет поставленной цели и пробежит за день не менее {goal} км.")
